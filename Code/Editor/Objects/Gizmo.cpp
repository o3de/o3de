#include "EditorDefs.h"

#include "Gizmo.h"

//////////////////////////////////////////////////////////////////////////
// CGizmo implementation.
//////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////
CGizmo::CGizmo()
{
    m_bDelete = false;
    m_matrix.SetIdentity();
    m_flags = 0;
}

//////////////////////////////////////////////////////////////////////////
CGizmo::~CGizmo()
{
}

//////////////////////////////////////////////////////////////////////////
void CGizmo::SetMatrix(const Matrix34& tm)
{
    m_matrix = tm;
}

//////////////////////////////////////////////////////////////////////////
void CGizmo::DeleteThis()
{
    m_bDelete = true;
};
